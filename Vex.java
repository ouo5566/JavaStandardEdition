public class Vex{
	public static void main(String[] args){
		boolean b = true ;
		int i = 3 ;
		char c = '3' ;
		String s = "3" ;
		double d = 3.0 ;

		System.out.println("boolean 타입의 value는 "+ b +"입니다.");
		System.out.println("int 타입의 value는 "+ i +"입니다.");
		System.out.println("char 타입의 value는 "+ c +"입니다.");
		System.out.println("String 타입의 value는 "+ s +"입니다.");
		System.out.println("double 타입의 value는 "+ d +"입니다.");
	
		System.out.println() ;

		b = false ;
		i = 4 ;
		c = '4' ;
		s = "4" ;
		d = 4.0 ;

		System.out.println("boolean 타입의 value는 "+ b +"입니다.");
		System.out.println("int 타입의 value는 "+ i +"입니다.");
		System.out.println("char 타입의 value는 "+ c +"입니다.");
		System.out.println("String 타입의 value는 "+ s +"입니다.");
		System.out.println("double 타입의 value는 "+ d +"입니다.");
	
	}
}